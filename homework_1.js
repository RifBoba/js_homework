// Створюємо масив
let arr = [1, 2, 3, 4, 5, 6, 7, 8, 9];

// Функція для перемішування масиву
function myBlend(arr) {
    // Проходимо по всьому масиву від початку до кінця
    for (let i = 0; i < arr.length; i++) {
        // Вибираємо випадковий індекс в межах масиву
        let j = Math.floor(Math.random() * arr.length);
        
        // Зберігаємо поточний елемент в тимчасову змінну
        let temp = arr[i];
        
        // Переміщуємо випадковий елемент на місце поточного
        arr[i] = arr[j];
        
        // Переміщуємо поточний елемент (збережений в тимчасовій змінній) на місце випадкового
        arr[j] = temp;
    }
}

// Викликаємо функцію myBlend з масивом як аргументом
myBlend(arr);

// Виводимо масив
console.log(arr);