// Отримання року від користувача
const year = prompt("Введіть рік:");

// Перевірка, що введене значення є числом та не є від'ємним
if (!isNaN(year) && year >= 0) {
    // Визначення правильного слова для числа "рік"
    let word;
    if (year % 10 === 1 && year % 100 !== 11) {
        word = "рік";
    } else if (year % 10 >= 2 && year % 10 <= 4 && (year % 100 < 10 || year % 100 >= 20)) {
        word = "роки";
    } else {
        word = "років";
    }

    // Виведення результату
    console.log(`Ваш рік: ${year} ${word}`);
} else {
    console.log("Будь ласка, введіть додатне число.");
}